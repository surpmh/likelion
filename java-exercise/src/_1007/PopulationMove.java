package _1007;

public class PopulationMove {
    int fromCity;
    int toCity;
    public PopulationMove(int fromCity, int toCity) {
        this.fromCity = fromCity;
        this.toCity = toCity;
    }

    public int getFromCity() {
        return fromCity;
    }

    public int getToCity() {
        return toCity;
    }
}
